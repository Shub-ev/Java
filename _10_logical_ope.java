public class _10_logical_ope {
    public static void main(String args[]){
        // ******  Logical operators  ******
        // returns boolean value by evaluating both operands

        System.out.println(true && true); // &&   Logical And Operator    (both true)
        System.out.println(false || true); // ||   Logical OR Operator    (one true)
        System.out.println(!true);        // !    Logical Not Operator    (true-> false or vice versa)
    }
}
