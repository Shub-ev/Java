public class _07_assignment_oper {
    public static void main(String args[]){
        // ******  Assignment operators  ******

        // A. Single Assignment Operator
        int a = 10; // this is simple assignment operator
        System.out.println(a);


        // B. Compund Assignment Operator
        System.out.println(a += 4); // a = a + 4
        System.out.println(a -= 4); // a = a - 4
        System.out.println(a *= 4); // a = a * 4
        System.out.println(a /= 4); // a = a + 4
        System.out.println(a %= 4); // a = a % 4
    }
}
