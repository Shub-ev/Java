public class _13_if_else {
    public static void main(String args[]){
        // ******  IF-ELSE blocks  ******

        boolean a = true;

        if(a){
            System.out.println("This is if block");
        }
        else if(a!=true){
            System.out.println("This is else if block");
        }
        else{
            System.out.println("This is else block");
        }
    }
}
