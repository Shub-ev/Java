public class _09_relational_ope {
    public static void main(String args[]) {
        // ******  Relational operators  ******
        // boolean result based on relation between two operands

        System.out.println(10 > 5); // >    greater than
        System.out.println(10 < 5); // <    less than
        System.out.println(10 >= 5); // >=      greater than equal to
        System.out.println(10 <= 5); // <=     lessthan equal to
        System.out.println(10 != 5); // !=     not equal to
        System.out.println(10 == 5); // ==     exactly equal to
    }
}
